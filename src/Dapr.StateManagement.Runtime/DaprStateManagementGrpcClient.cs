        mode switch
        {
            ConcurrencyMode.FirstWrite => Autogenerated.StateOptions.Types.StateConcurrency.ConcurrencyFirstWrite,
            ConcurrencyMode.LastWrite => Autogenerated.StateOptions.Types.StateConcurrency.ConcurrencyLastWrite,
            _ => Autogenerated.StateOptions.Types.StateConcurrency.ConcurrencyUnspecified,
        };

    /// <summary>
    /// Converts a <see cref="StateOptions"/> to the autogenerated gRPC equivalent.
    /// </summary>
    private static Autogenerated.StateOptions ToGrpcStateOptions(StateOptions options)
    {
        var grpcOptions = new Autogenerated.StateOptions();

        if (options.Consistency is not null)
            grpcOptions.Consistency = ToGrpcConsistency(options.Consistency.Value);

        if (options.Concurrency is not null)
            grpcOptions.Concurrency = ToGrpcConcurrency(options.Concurrency.Value);

        return grpcOptions;
    }

