    /// <summary>
    /// Converts a <see cref="ConsistencyMode"/> to the autogenerated gRPC equivalent.
    /// </summary>
    private static Autogenerated.StateOptions.Types.StateConsistency ToGrpcConsistency(ConsistencyMode mode) =>
        mode switch
        {
            ConsistencyMode.Eventual => Autogenerated.StateOptions.Types.StateConsistency.ConsistencyEventual,
            ConsistencyMode.Strong => Autogenerated.StateOptions.Types.StateConsistency.ConsistencyStrong,
            _ => Autogenerated.StateOptions.Types.StateConsistency.ConsistencyUnspecified,
        };

    /// <summary>
    /// Converts a <see cref="ConcurrencyMode"/> to the autogenerated gRPC equivalent.
    /// </summary>
