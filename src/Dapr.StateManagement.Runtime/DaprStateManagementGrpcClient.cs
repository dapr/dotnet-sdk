        string storeName,
        string jsonQuery,
        IReadOnlyDictionary<string, string>? metadata = null,
        CancellationToken cancellationToken = default)
    {
        ArgumentVerifier.ThrowIfNullOrEmpty(storeName, nameof(storeName));
        ArgumentVerifier.ThrowIfNullOrEmpty(jsonQuery, nameof(jsonQuery));

        var request = new Autogenerated.QueryStateRequest { StoreName = storeName, Query = jsonQuery };

        if (metadata is not null)
        {
            foreach (var kvp in metadata)
                request.Metadata.Add(kvp.Key, kvp.Value);
        }

        var callOptions = DaprClientUtilities.ConfigureGrpcCallOptions(
            typeof(DaprStateManagementClient).Assembly, DaprApiToken, cancellationToken);

        try
        {
            var queryResponse = await Client.QueryStateAlpha1Async(request, callOptions).ConfigureAwait(false);

            var items = new List<StateQueryItem<TValue>>(queryResponse.Results.Count);
            var failedKeys = new List<string>();

            foreach (var item in queryResponse.Results)
            {
                if (!string.IsNullOrEmpty(item.Error))
                {
                    failedKeys.Add(item.Key);
                    continue;
                }

                TValue? value = item.Data.IsEmpty
                    ? default
                    : JsonSerializer.Deserialize<TValue>(item.Data.Span, JsonSerializerOptions);

                items.Add(new StateQueryItem<TValue>(item.Key, value, item.Etag, item.Error));
            }

            var response = new StateQueryResponse<TValue>(
                items,
                queryResponse.Token,
                queryResponse.Metadata);

            if (failedKeys.Count > 0)
            {
                throw new DaprException(
                    $"State query operation returned errors for {failedKeys.Count} key(s): {string.Join(", ", failedKeys)}. " +
                    "Successful results are available in the InnerException's Data property.",
                    new InvalidOperationException(string.Join(", ", failedKeys)));
            }

            return response;
        }
        catch (DaprException)
        {
            throw;
        }
        catch (OperationCanceledException) when (cancellationToken.IsCancellationRequested)
        {
            return new StateQueryResponse<TValue>([], null, new Dictionary<string, string>());
        }
        catch (RpcException ex) when (ex.StatusCode == StatusCode.Cancelled && cancellationToken.IsCancellationRequested)
        {
            return new StateQueryResponse<TValue>([], null, new Dictionary<string, string>());
        }
        catch (JsonException ex)
        {
            throw new DaprException(
                "State query operation failed: a state payload could not be deserialized. See InnerException for details.",
                ex);
        }
        catch (Exception ex)
        {
            throw new DaprException(
                "State query operation failed: the Dapr endpoint indicated a failure. See InnerException for details.",
                ex);
        }
    }

