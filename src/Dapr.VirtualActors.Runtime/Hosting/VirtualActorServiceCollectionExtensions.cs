using Dapr.Common.DependencyInjection;
using Dapr.Common.Serialization;
using Dapr.VirtualActors.Runtime.Client;
using Dapr.VirtualActors.Runtime.Grpc;
using Microsoft.Extensions.Configuration;
using Microsoft.Extensions.DependencyInjection;
using Microsoft.Extensions.DependencyInjection.Extensions;
using Microsoft.Extensions.Logging;
using Microsoft.Extensions.Options;
using Autogenerated = Dapr.Client.Autogen.Grpc.v1;

namespace Dapr.VirtualActors.Runtime;

/// <summary>
/// Extension methods for registering Dapr Virtual Actors with the DI container.
/// </summary>
public static class VirtualActorServiceCollectionExtensions
{
    /// <summary>
    /// Adds Dapr Virtual Actors support to the service collection.
    /// </summary>
    /// <param name="services">The service collection.</param>
    /// <param name="configure">A delegate to configure actor options (register actors, set timeouts, etc.).</param>
    /// <returns>A <see cref="VirtualActorBuilder"/> for further configuration.</returns>
    /// <example>
    /// <code>
    /// services.AddDaprVirtualActors(options =>
    /// {
    ///     options.RegisterActor&lt;MyActor&gt;();
    ///     options.Reentrancy.Enabled = true;
    /// })
    /// .UseMiddleware&lt;MetricsMiddleware&gt;()
    /// .AddLifecycleObserver&lt;AuditObserver&gt;();
    /// </code>
    /// </example>
    public static VirtualActorBuilder AddDaprVirtualActors(
        this IServiceCollection services,
        Action<VirtualActorOptions> configure)
    {
        ArgumentNullException.ThrowIfNull(services);
        ArgumentNullException.ThrowIfNull(configure);

        // Register shared Dapr services
        services.AddDaprCoreServices();
        services.AddLogging();

        // Configure options
        services.Configure(configure);

        // Post-configure to fill in defaults from environment/config
        services.AddSingleton<IPostConfigureOptions<VirtualActorOptions>, VirtualActorOptionsPostConfigure>();

        // Register the gRPC client for Dapr sidecar communication
        RegisterGrpcClient(services);

        // Register core runtime services
        services.TryAddSingleton<DaprGrpcActorClient>(sp =>
        {
            var grpcClient = sp.GetRequiredService<Autogenerated.Dapr.DaprClient>();
            var serializer = sp.GetRequiredService<IDaprSerializer>();
            var logger = sp.GetRequiredService<ILogger<DaprGrpcActorClient>>();
            var options = sp.GetRequiredService<IOptions<VirtualActorOptions>>().Value;
            return new DaprGrpcActorClient(grpcClient, serializer, logger, options.DaprApiToken);
        });

        // Actor registration registry — populated from options, queried at activation time
        services.TryAddSingleton<ActorRegistrationRegistry>(sp =>
        {
            var options = sp.GetRequiredService<IOptions<VirtualActorOptions>>().Value;
            var registry = new ActorRegistrationRegistry();
            foreach (var registration in options.ActorRegistrations)
            {
                registry.Register(registration);
            }
            return registry;
        });

        // Timer manager — pluggable via builder.UseTimerManager<T>()
        services.TryAddSingleton<IActorTimerManager>(sp => sp.GetRequiredService<DaprGrpcActorClient>());

        // Actor activator — pluggable via builder.UseActorActivator<T>()
        services.TryAddSingleton<IActorActivator, DependencyInjectionActorActivator>();

        // Proxy factory — pluggable
        services.TryAddSingleton<IVirtualActorProxyFactory, GrpcVirtualActorProxyFactory>();

        // Host factory (scoped — one per actor activation)
        services.TryAddScoped<VirtualActorHostFactory>(sp =>
        {
            var grpcClient = sp.GetRequiredService<DaprGrpcActorClient>();
            var timerManager = sp.GetRequiredService<IActorTimerManager>();
            var proxyFactory = sp.GetRequiredService<IVirtualActorProxyFactory>();
            var loggerFactory = sp.GetRequiredService<ILoggerFactory>();
            return new VirtualActorHostFactory(grpcClient, timerManager, proxyFactory, loggerFactory);
        });

        return new VirtualActorBuilder(services);
    }

    /// <summary>
    /// Adds Dapr Virtual Actors support with default options (no actors pre-registered).
    /// </summary>
    /// <param name="services">The service collection.</param>
    /// <returns>A <see cref="VirtualActorBuilder"/> for further configuration.</returns>
    public static VirtualActorBuilder AddDaprVirtualActors(this IServiceCollection services)
    {
        return services.AddDaprVirtualActors(_ => { });
    }

    private static void RegisterGrpcClient(IServiceCollection services)
    {
        services.AddGrpcClient<Autogenerated.Dapr.DaprClient>(
            (serviceProvider, options) =>
            {
                var actorOptions = serviceProvider.GetRequiredService<IOptions<VirtualActorOptions>>().Value;

                // GrpcEndpoint is populated by VirtualActorOptionsPostConfigure
                var endpoint = actorOptions.GrpcEndpoint ?? "http://localhost:50001";

                options.Address = new Uri(endpoint);

                // Configure for persistent long-lived connections (like Dapr.Workflow)
                options.ChannelOptionsActions.Add(channelOptions =>
                {
                    channelOptions.HttpHandler = new SocketsHttpHandler
                    {
                        PooledConnectionIdleTimeout = Timeout.InfiniteTimeSpan,
                        KeepAlivePingDelay = TimeSpan.FromSeconds(60),
                        KeepAlivePingTimeout = TimeSpan.FromSeconds(30),
                        EnableMultipleHttp2Connections = true,
                        KeepAlivePingPolicy = HttpKeepAlivePingPolicy.Always,
                    };

                    channelOptions.MaxReceiveMessageSize = null;
                    channelOptions.MaxSendMessageSize = null;
                });
            })
        .ConfigurePrimaryHttpMessageHandler(() => new SocketsHttpHandler
        {
            ConnectTimeout = Timeout.InfiniteTimeSpan,
            PooledConnectionIdleTimeout = Timeout.InfiniteTimeSpan,
            PooledConnectionLifetime = Timeout.InfiniteTimeSpan,
            KeepAlivePingDelay = TimeSpan.FromSeconds(60),
            KeepAlivePingTimeout = TimeSpan.FromSeconds(30),
            KeepAlivePingPolicy = HttpKeepAlivePingPolicy.Always,
            EnableMultipleHttp2Connections = true,
        })
        .ConfigureHttpClient(httpClient =>
        {
            // Eliminate the 100-second default timeout
            httpClient.Timeout = Timeout.InfiniteTimeSpan;
        });
    }
}

/// <summary>
/// Post-configures <see cref="VirtualActorOptions"/> with defaults from environment/configuration.
/// </summary>
internal sealed class VirtualActorOptionsPostConfigure : IPostConfigureOptions<VirtualActorOptions>
{
    private readonly IConfiguration? _configuration;

    public VirtualActorOptionsPostConfigure(IConfiguration? configuration = null)
    {
        _configuration = configuration;
    }

    public void PostConfigure(string? name, VirtualActorOptions options)
    {
        DaprServiceCollectionExtensions.PopulateDefaults(options, _configuration);

        // Apply auto-discovered actor registrations from source generators
        VirtualActorAutoRegistration.ApplyDiscoveryHooks(options);
    }
}
