using System;
using System.IO;
using System.Net.Http;
using System.Threading;
using System.Threading.Tasks;
using System.Collections.Generic;
using Dapr.Cryptography.Encryption;
using Dapr.Cryptography.Encryption.Models;
using Moq;
using Autogenerated = Dapr.Client.Autogen.Grpc.v1;

namespace Dapr.Cryptography.Test.Encryption;

public class DaprEncryptionClientTests
{
    private sealed class TestDaprEncryptionClient(
        Autogenerated.Dapr.DaprClient client,
        HttpClient httpClient,
        string? daprApiToken = null)
        : DaprEncryptionClient(client, httpClient, daprApiToken)
    {
        public override Task<ReadOnlyMemory<byte>> EncryptAsync(
            string vaultResourceName, ReadOnlyMemory<byte> plaintextBytes, string keyName,
            EncryptionOptions encryptionOptions, CancellationToken cancellationToken = default) =>
            throw new NotImplementedException();

        public override IAsyncEnumerable<ReadOnlyMemory<byte>> EncryptAsync(
            string vaultResourceName, Stream plaintextStream, string keyName,
            EncryptionOptions encryptionOptions, CancellationToken cancellationToken = default) =>
            throw new NotImplementedException();

        public override Task<ReadOnlyMemory<byte>> DecryptAsync(
            string vaultResourceName, ReadOnlyMemory<byte> ciphertextBytes, string keyName,
            DecryptionOptions? options = null, CancellationToken cancellationToken = default) =>
            throw new NotImplementedException();

        public override IAsyncEnumerable<ReadOnlyMemory<byte>> DecryptAsync(
            string vaultResourceName, Stream ciphertextStream, string keyName,
            DecryptionOptions? options = null, CancellationToken cancellationToken = default) =>
            throw new NotImplementedException();
    }

    private static TestDaprEncryptionClient CreateClient(string? apiToken = null)
    {
        var mockDaprClient = new Mock<Autogenerated.Dapr.DaprClient>();
        var httpClient = new HttpClient();
        return new TestDaprEncryptionClient(mockDaprClient.Object, httpClient, apiToken);
    }

    [Fact]
    public void GetDaprApiTokenHeader_WithNullToken_ReturnsNull()
    {
        Assert.Null(DaprEncryptionClient.GetDaprApiTokenHeader(null!));
    }

    [Fact]
    public void GetDaprApiTokenHeader_WithEmptyToken_ReturnsNull()
    {
        Assert.Null(DaprEncryptionClient.GetDaprApiTokenHeader(""));
    }

    [Fact]
    public void GetDaprApiTokenHeader_WithWhitespaceToken_ReturnsNull()
    {
        Assert.Null(DaprEncryptionClient.GetDaprApiTokenHeader("   "));
    }

    [Fact]
    public void GetDaprApiTokenHeader_WithValidToken_ReturnsHeader()
    {
        var result = DaprEncryptionClient.GetDaprApiTokenHeader("my-api-token");
        Assert.NotNull(result);
        Assert.Equal("dapr-api-token", result.Value.Key);
        Assert.Equal("my-api-token", result.Value.Value);
    }

    [Fact]
    public void Dispose_IsIdempotent()
    {
        var client = CreateClient();
        client.Dispose();
        client.Dispose(); // Should not throw
    }

    [Fact]
    public void Constructor_StoresHttpClient()
    {
        var mockDaprClient = new Mock<Autogenerated.Dapr.DaprClient>();
        var httpClient = new HttpClient();
        var client = new TestDaprEncryptionClient(mockDaprClient.Object, httpClient);
        Assert.Same(httpClient, client.HttpClient);
    }

    [Fact]
    public void Constructor_StoresDaprApiToken()
    {
        var client = CreateClient("test-token");
        Assert.Equal("test-token", client.DaprApiToken);
    }

    [Fact]
    public void Constructor_StoresDaprApiToken_WhenNull()
    {
        var client = CreateClient(null);
        Assert.Null(client.DaprApiToken);
    }

    [Fact]
    public void Constructor_StoresDaprClient()
    {
        var mockDaprClient = new Mock<Autogenerated.Dapr.DaprClient>();
        var httpClient = new HttpClient();
        var client = new TestDaprEncryptionClient(mockDaprClient.Object, httpClient);
        Assert.Same(mockDaprClient.Object, client.Client);
    }
}
