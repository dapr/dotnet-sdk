    [Fact]
    public async Task EncryptAsync_Stream_CallsEncryptAlpha1()
    {
        var mockRequestStream = new Mock<IClientStreamWriter<Autogenerated.EncryptRequest>>();
        mockRequestStream
            .Setup(x => x.WriteAsync(It.IsAny<Autogenerated.EncryptRequest>(), It.IsAny<CancellationToken>()))
            .Returns(Task.CompletedTask);
        mockRequestStream.Setup(x => x.CompleteAsync()).Returns(Task.CompletedTask);

        var responseData = ByteString.CopyFrom(99, 98, 97);
        var mockResponseStream = new Mock<IAsyncStreamReader<Autogenerated.EncryptResponse>>();
        mockResponseStream
            .SetupSequence(x => x.MoveNext(It.IsAny<CancellationToken>()))
            .ReturnsAsync(true)
            .ReturnsAsync(false);
        mockResponseStream
            .Setup(x => x.Current)
            .Returns(new Autogenerated.EncryptResponse
            {
                Payload = new Autogenerated.StreamPayload { Data = responseData }
            });

        var mockDaprClient = new Mock<Autogenerated.Dapr.DaprClient>();
        mockDaprClient
            .Setup(x => x.EncryptAlpha1(It.IsAny<CallOptions>()))
            .Returns(CreateEncryptCall(mockRequestStream, mockResponseStream));

        var client = CreateClient(mockDaprClient);
        var options = new EncryptionOptions(KeyWrapAlgorithm.Aes);

        using var inputStream = new MemoryStream(new byte[] { 1, 2, 3 });
        var results = new List<ReadOnlyMemory<byte>>();

        await foreach (var chunk in client.EncryptAsync("vault", inputStream, "key", options, TestContext.Current.CancellationToken))
        {
            results.Add(chunk);
        }

        Assert.Single(results);
        Assert.Equal(new byte[] { 99, 98, 97 }, results[0].ToArray());
        mockDaprClient.Verify(x => x.EncryptAlpha1(It.IsAny<CallOptions>()), Times.Once);
    }

