    {
        return new AsyncDuplexStreamingCall<Autogenerated.EncryptRequest, Autogenerated.EncryptResponse>(
            requestStream.Object, responseStream.Object,
            Task.FromResult(new Metadata()), () => Status.DefaultSuccess,
            () => new Metadata(), () => { });
