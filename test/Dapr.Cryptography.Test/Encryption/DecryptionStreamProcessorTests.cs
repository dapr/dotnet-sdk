    [Fact]
    public async Task ProcessStreamAsync_WhenRequestStreamThrows_RaisesExceptionEvent()
    {
        var mockRequestStream = new Mock<IClientStreamWriter<Autogenerated.DecryptRequest>>();
        var expectedException = new InvalidOperationException("write error");
        mockRequestStream
            .Setup(x => x.WriteAsync(It.IsAny<Autogenerated.DecryptRequest>(), It.IsAny<CancellationToken>()))
            .ThrowsAsync(expectedException);
        mockRequestStream
            .Setup(x => x.CompleteAsync())
            .Returns(Task.CompletedTask);

        var mockResponseStream = new Mock<IAsyncStreamReader<Autogenerated.DecryptResponse>>();
        mockResponseStream
            .Setup(x => x.MoveNext(It.IsAny<CancellationToken>()))
            .ReturnsAsync(false);

        var call = CreateMockCall(mockRequestStream, mockResponseStream);
        var options = new Autogenerated.DecryptRequestOptions { ComponentName = "vault", KeyName = "key" };

        using var inputStream = new MemoryStream(new byte[] { 1, 2, 3 });

        var exceptionTcs = new TaskCompletionSource<Exception>();

        using var processor = new DecryptionStreamProcessor();
        processor.OnException += (_, ex) => exceptionTcs.TrySetResult(ex);

        await processor.ProcessStreamAsync(inputStream, call, 4096, options, TestContext.Current.CancellationToken);

        var receivedException = await exceptionTcs.Task.WaitAsync(TimeSpan.FromSeconds(5), TestContext.Current.CancellationToken);
        Assert.Same(expectedException, receivedException);
    }

    [Fact]
    public async Task ProcessStreamAsync_MultipleResponses_ReturnsAllChunks()
    {
        var mockRequestStream = new Mock<IClientStreamWriter<Autogenerated.DecryptRequest>>();
        mockRequestStream
            .Setup(x => x.WriteAsync(It.IsAny<Autogenerated.DecryptRequest>(), It.IsAny<CancellationToken>()))
            .Returns(Task.CompletedTask);
        mockRequestStream
            .Setup(x => x.CompleteAsync())
            .Returns(Task.CompletedTask);

        var responses = new[]
        {
            new Autogenerated.DecryptResponse
                { Payload = new Autogenerated.StreamPayload { Data = ByteString.CopyFrom(1, 2), Seq = 0 } },
            new Autogenerated.DecryptResponse
                { Payload = new Autogenerated.StreamPayload { Data = ByteString.CopyFrom(3, 4), Seq = 1 } },
            new Autogenerated.DecryptResponse
                { Payload = new Autogenerated.StreamPayload { Data = ByteString.CopyFrom(5, 6), Seq = 2 } }
        };

        var responseIndex = -1;
        var mockResponseStream = new Mock<IAsyncStreamReader<Autogenerated.DecryptResponse>>();
        mockResponseStream
            .Setup(x => x.MoveNext(It.IsAny<CancellationToken>()))
            .ReturnsAsync(() => ++responseIndex < responses.Length);
        mockResponseStream
            .Setup(x => x.Current)
            .Returns(() => responses[responseIndex]);

        var call = CreateMockCall(mockRequestStream, mockResponseStream);
        var options = new Autogenerated.DecryptRequestOptions { ComponentName = "vault", KeyName = "key" };

        using var inputStream = new MemoryStream(new byte[] { 10, 20, 30, 40, 50, 60 });

        using var processor = new DecryptionStreamProcessor();
        await processor.ProcessStreamAsync(inputStream, call, 2, options, TestContext.Current.CancellationToken);

        var results = new List<byte[]>();
        await foreach (var item in processor.GetProcessedDataAsync(TestContext.Current.CancellationToken))
        {
            results.Add(item.ToArray());
        }

        Assert.Equal(3, results.Count);
        Assert.Equal(new byte[] { 1, 2 }, results[0]);
        Assert.Equal(new byte[] { 3, 4 }, results[1]);
        Assert.Equal(new byte[] { 5, 6 }, results[2]);
    }
}
