#nullable enable
using System.Net.Http;
using System.Threading;
using System.Threading.Tasks;
using Dapr.DistributedLock.Models;
using Moq;
using Autogenerated = Dapr.Client.Autogen.Grpc.v1;

namespace Dapr.DistributedLock.Test;

public class DaprDistributedLockClientTest
{
    private sealed class FakeLockClient(
        Autogenerated.Dapr.DaprClient client,
        HttpClient httpClient,
        string daprApiToken)
        : DaprDistributedLockClient(client, httpClient, daprApiToken)
    {
        public override Task<LockResponse?> TryLockAsync(
            string storeName, string resourceId, string lockOwner,
            int expiryInSeconds, CancellationToken cancellationToken = default) =>
            Task.FromResult<LockResponse?>(null);

        public override Task<UnlockResponse> TryUnlockAsync(
            string storeName, string resourceId, string lockOwner,
            CancellationToken cancellationToken = default) =>
            Task.FromResult(new UnlockResponse(LockStatus.Success));
    }

    [Fact]
    public void DaprDistributedLockClient_StoresGrpcClient()
    {
        var grpcClient = Mock.Of<Autogenerated.Dapr.DaprClient>();
        var client = new FakeLockClient(grpcClient, new HttpClient(), string.Empty);

        Assert.Same(grpcClient, client.Client);
    }

    [Fact]
    public void DaprDistributedLockClient_StoresDaprApiToken()
    {
        var grpcClient = Mock.Of<Autogenerated.Dapr.DaprClient>();
        var client = new FakeLockClient(grpcClient, new HttpClient(), "my-token");

        Assert.Equal("my-token", client.DaprApiToken);
    }

    [Fact]
    public void DaprDistributedLockClient_EmptyDaprApiToken()
    {
        var grpcClient = Mock.Of<Autogenerated.Dapr.DaprClient>();
        var client = new FakeLockClient(grpcClient, new HttpClient(), string.Empty);

        Assert.Equal(string.Empty, client.DaprApiToken);
    }

    [Fact]
    public void DaprDistributedLockClient_Dispose_CanBeCalledMultipleTimes()
    {
        var grpcClient = Mock.Of<Autogenerated.Dapr.DaprClient>();
        var client = new FakeLockClient(grpcClient, new HttpClient(), string.Empty);

        // Dispose twice — should not throw
        client.Dispose();
        client.Dispose();
    }

    [Fact]
    public void DaprDistributedLockClient_ImplementsIDaprDistributedLockClient()
    {
        var grpcClient = Mock.Of<Autogenerated.Dapr.DaprClient>();
        var client = new FakeLockClient(grpcClient, new HttpClient(), string.Empty);

        Assert.IsAssignableFrom<IDaprDistributedLockClient>(client);
    }
}
