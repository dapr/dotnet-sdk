#nullable enable
using System;
using System.Net.Http;
using System.Threading.Tasks;
using Moq;
using Autogenerated = Dapr.Client.Autogen.Grpc.v1;

namespace Dapr.DistributedLock.Test;

public class DaprDistributedLockGrpcClientTest
{
    private static DaprDistributedLockGrpcClient CreateClient()
    {
        var grpcClient = Mock.Of<Autogenerated.Dapr.DaprClient>();
        return new DaprDistributedLockGrpcClient(grpcClient, new HttpClient(), string.Empty);
    }

    #region TryLockAsync argument validation

    [Fact]
    public async Task TryLockAsync_NullStoreName_ThrowsArgumentNullException()
    {
        var client = CreateClient();
        await Assert.ThrowsAsync<ArgumentNullException>(() =>
            client.TryLockAsync(null!, "resource", "owner", 30, TestContext.Current.CancellationToken));
    }

    [Fact]
    public async Task TryLockAsync_EmptyStoreName_ThrowsArgumentException()
    {
        var client = CreateClient();
        await Assert.ThrowsAsync<ArgumentException>(() =>
            client.TryLockAsync(string.Empty, "resource", "owner", 30, TestContext.Current.CancellationToken));
    }

    [Fact]
    public async Task TryLockAsync_NullResourceId_ThrowsArgumentNullException()
    {
        var client = CreateClient();
        await Assert.ThrowsAsync<ArgumentNullException>(() =>
            client.TryLockAsync("store", null!, "owner", 30, TestContext.Current.CancellationToken));
    }

    [Fact]
    public async Task TryLockAsync_EmptyResourceId_ThrowsArgumentException()
    {
        var client = CreateClient();
        await Assert.ThrowsAsync<ArgumentException>(() =>
            client.TryLockAsync("store", string.Empty, "owner", 30, TestContext.Current.CancellationToken));
    }

    [Fact]
    public async Task TryLockAsync_NullLockOwner_ThrowsArgumentNullException()
    {
        var client = CreateClient();
        await Assert.ThrowsAsync<ArgumentNullException>(() =>
            client.TryLockAsync("store", "resource", null!, 30, TestContext.Current.CancellationToken));
    }

    [Fact]
    public async Task TryLockAsync_EmptyLockOwner_ThrowsArgumentException()
    {
        var client = CreateClient();
        await Assert.ThrowsAsync<ArgumentException>(() =>
            client.TryLockAsync("store", "resource", string.Empty, 30, TestContext.Current.CancellationToken));
    }

    [Theory]
    [InlineData(0)]
    [InlineData(-1)]
    [InlineData(-100)]
    public async Task TryLockAsync_NonPositiveExpiry_ThrowsArgumentOutOfRangeException(int expiry)
    {
        var client = CreateClient();
        await Assert.ThrowsAsync<ArgumentOutOfRangeException>(() =>
            client.TryLockAsync("store", "resource", "owner", expiry, TestContext.Current.CancellationToken));
    }

    [Fact]
    public async Task TryLockAsync_PositiveExpiry_DoesNotThrowArgumentOutOfRangeException()
    {
        var client = CreateClient();
        // Validation passes; the exception thrown (if any) is NOT ArgumentOutOfRangeException.
        var ex = await Record.ExceptionAsync(() =>
            client.TryLockAsync("store", "resource", "owner", 1, TestContext.Current.CancellationToken));
        Assert.IsNotType<ArgumentOutOfRangeException>(ex);
    }

    #endregion

    #region TryUnlockAsync argument validation

    [Fact]
    public async Task TryUnlockAsync_NullStoreName_ThrowsArgumentNullException()
    {
        var client = CreateClient();
        await Assert.ThrowsAsync<ArgumentNullException>(() =>
            client.TryUnlockAsync(null!, "resource", "owner", TestContext.Current.CancellationToken));
    }

    [Fact]
    public async Task TryUnlockAsync_EmptyStoreName_ThrowsArgumentException()
    {
        var client = CreateClient();
        await Assert.ThrowsAsync<ArgumentException>(() =>
            client.TryUnlockAsync(string.Empty, "resource", "owner", TestContext.Current.CancellationToken));
    }

    [Fact]
    public async Task TryUnlockAsync_NullResourceId_ThrowsArgumentNullException()
    {
        var client = CreateClient();
        await Assert.ThrowsAsync<ArgumentNullException>(() =>
            client.TryUnlockAsync("store", null!, "owner", TestContext.Current.CancellationToken));
    }

    [Fact]
    public async Task TryUnlockAsync_EmptyResourceId_ThrowsArgumentException()
    {
        var client = CreateClient();
        await Assert.ThrowsAsync<ArgumentException>(() =>
            client.TryUnlockAsync("store", string.Empty, "owner", TestContext.Current.CancellationToken));
    }

    [Fact]
    public async Task TryUnlockAsync_NullLockOwner_ThrowsArgumentNullException()
    {
        var client = CreateClient();
        await Assert.ThrowsAsync<ArgumentNullException>(() =>
            client.TryUnlockAsync("store", "resource", null!, TestContext.Current.CancellationToken));
    }

    [Fact]
    public async Task TryUnlockAsync_EmptyLockOwner_ThrowsArgumentException()
    {
        var client = CreateClient();
        await Assert.ThrowsAsync<ArgumentException>(() =>
            client.TryUnlockAsync("store", "resource", string.Empty, TestContext.Current.CancellationToken));
    }

    [Fact]
    public async Task TryUnlockAsync_ValidArgs_DoesNotThrowArgumentException()
    {
        var client = CreateClient();
        // Validation passes; the exception thrown (if any) is NOT an ArgumentException.
        var ex = await Record.ExceptionAsync(() =>
            client.TryUnlockAsync("store", "resource", "owner", TestContext.Current.CancellationToken));
        Assert.IsNotType<ArgumentException>(ex);
        Assert.IsNotType<ArgumentNullException>(ex);
    }

    #endregion
}
