    #region Metadata Passthrough

    [Fact]
    public async Task GetSecretAsync_PassesMetadataToGrpcRequest()
    {
        var response = new Autogenerated.GetSecretResponse();
        response.Data.Add("key", "value");

        Autogenerated.GetSecretRequest? capturedRequest = null;
        var mock = new Mock<Autogenerated.Dapr.DaprClient>();
        mock.Setup(m => m.GetSecretAsync(
                It.IsAny<Autogenerated.GetSecretRequest>(),
                It.IsAny<CallOptions>()))
            .Callback<Autogenerated.GetSecretRequest, CallOptions>((req, _) => capturedRequest = req)
            .Returns(new AsyncUnaryCall<Autogenerated.GetSecretResponse>(
                Task.FromResult(response),
                Task.FromResult(new Metadata()),
                () => Status.DefaultSuccess,
                () => new Metadata(),
                () => { }));

        var client = new DaprSecretsManagementGrpcClient(mock.Object, new HttpClient(), null!, null);
        var metadata = new Dictionary<string, string>
        {
            { "version", "2" },
            { "namespace", "production" }
        };

        await client.GetSecretAsync("my-store", "my-key", metadata, TestContext.Current.CancellationToken);

        Assert.NotNull(capturedRequest);
        Assert.Equal("my-store", capturedRequest!.StoreName);
        Assert.Equal("my-key", capturedRequest.Key);
        Assert.Equal(2, capturedRequest.Metadata.Count);
        Assert.Equal("2", capturedRequest.Metadata["version"]);
        Assert.Equal("production", capturedRequest.Metadata["namespace"]);
    }

