    [Fact]
    public async Task Dispose_DisposesHttpClient()
    {
        var mockClient = Mock.Of<Autogenerated.Dapr.DaprClient>();
        var httpClient = new HttpClient();

        var client = new DaprSecretsManagementGrpcClient(mockClient, httpClient, null);
        client.Dispose();

        // After disposing, the underlying HttpClient should be disposed —
        // attempting to use it should throw ObjectDisposedException.
        await Assert.ThrowsAsync<ObjectDisposedException>(async () =>
            await httpClient.GetAsync("http://localhost", TestContext.Current.CancellationToken));
