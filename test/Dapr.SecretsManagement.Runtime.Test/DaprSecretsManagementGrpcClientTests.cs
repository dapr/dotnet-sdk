    [Fact]
    public async Task GetSecretAsync_ReturnsSecretFromGrpcResponse()
    {
        var response = new Autogenerated.GetSecretResponse();
        response.Data.Add("connection-string", "Server=myserver;Database=mydb");

        var mock = new Mock<Autogenerated.Dapr.DaprClient>();
        mock.Setup(m => m.GetSecretAsync(
                It.IsAny<Autogenerated.GetSecretRequest>(),
                It.IsAny<CallOptions>()))
            .Returns(new AsyncUnaryCall<Autogenerated.GetSecretResponse>(
                Task.FromResult(response),
                Task.FromResult(new Metadata()),
                () => Status.DefaultSuccess,
                () => new Metadata(),
                () => { }));

        var client = new DaprSecretsManagementGrpcClient(mock.Object, new HttpClient(), null!, null);

        var result = await client.GetSecretAsync("my-store", "db-secret", cancellationToken: TestContext.Current.CancellationToken);

        Assert.Single(result);
