    private static DaprStateManagementGrpcClient CreateClient()
    {
        // Construct a gRPC channel pointing at a non-existent endpoint; we won't actually
        // call it in these unit tests — we only test argument validation paths.
        var channel = Grpc.Net.Client.GrpcChannel.ForAddress("http://localhost:50001");
        var grpcClient = new Autogenerated.Dapr.DaprClient(channel);
        return new DaprStateManagementGrpcClient(
            grpcClient,
            new HttpClient(),
            Mock.Of<IDaprRuntimeCapabilities>(),
            new JsonDaprSerializer(new JsonSerializerOptions(JsonSerializerDefaults.Web)));
    }

