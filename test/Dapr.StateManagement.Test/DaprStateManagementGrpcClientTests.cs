    // ── Model type tests ────────────────────────────────────────────────────

    [Fact]
    public void SaveStateItem_StoresAllProperties()
    {
        var metadata = new Dictionary<string, string> { ["key"] = "val" };
        var options = new StateOptions { Consistency = ConsistencyMode.Strong };
        var item = new SaveStateItem<string>("mykey", "myvalue", "etag1", options, metadata);

        Assert.Equal("mykey", item.Key);
        Assert.Equal("myvalue", item.Value);
        Assert.Equal("etag1", item.ETag);
        Assert.Same(options, item.StateOptions);
        Assert.Same(metadata, item.Metadata);
    }

    [Fact]
    public void BulkDeleteStateItem_StoresAllProperties()
    {
        var metadata = new Dictionary<string, string> { ["k"] = "v" };
        var options = new StateOptions { Concurrency = ConcurrencyMode.FirstWrite };
        var item = new BulkDeleteStateItem("k", "etag", options, metadata);

        Assert.Equal("k", item.Key);
        Assert.Equal("etag", item.ETag);
        Assert.Same(options, item.StateOptions);
        Assert.Same(metadata, item.Metadata);
    }

    [Fact]
    public void StateTransactionRequest_NullKey_Throws()
    {
        Assert.Throws<ArgumentNullException>(() =>
            new StateTransactionRequest(null!, null, StateOperationType.Upsert));
    }

    [Fact]
    public void StateQueryResponse_ExposesAllProperties()
    {
        var items = new List<StateQueryItem<string>>
        {
            new("k1", "v1", "e1", null)
        };
        var metadata = new Dictionary<string, string>();
        var response = new StateQueryResponse<string>(items, "token", metadata);

        Assert.Single(response.Results);
        Assert.Equal("token", response.Token);
        Assert.Same(metadata, response.Metadata);
    }
